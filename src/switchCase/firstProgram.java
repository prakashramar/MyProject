package switchCase;

public class firstProgram 
{
	public static void main(String [] args)
	{
		int a=10;
		
		switch(a)
		{
		case 5:
			System.out.println("56");
			break;
		case 6:
			System.out.println("65");
			break;
		case 10:
			System.out.println("106");
			break;
			default:
			System.out.println("Error Input");
			break;

		}
		
	}
} 
